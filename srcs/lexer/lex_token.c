#include "../includes/nsh.h"

void	abort_token(t_lex *l)
{
	while (l->j > 0)
	{
		free(l->t[l->j].data);
		l->j--;
	}
}

void	token_init(t_tok *tok)
{
	tok->data = NULL;
	tok->len = 0;
	tok->type = -1;
}
